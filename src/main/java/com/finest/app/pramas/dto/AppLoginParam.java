package com.finest.app.pramas.dto;

public class AppLoginParam {

    private String authData;
    private String bisData;
    private String cliData;
    private String sysCode;

    public String getAuthData() {
        return authData;
    }

    public void setAuthData(String authData) {
        this.authData = authData;
    }

    public String getBisData() {
        return bisData;
    }

    public void setBisData(String bisData) {
        this.bisData = bisData;
    }

    public String getCliData() {
        return cliData;
    }

    public void setCliData(String cliData) {
        this.cliData = cliData;
    }

    public String getSysCode() {
        return sysCode;
    }

    public void setSysCode(String sysCode) {
        this.sysCode = sysCode;
    }
}